async function seedTestimonials() {
  try {
    console.log('Seeding testimonials...')
    
    for (const testimonial of testimonialData) {
      const language = testimonial.message.match(/[a-zA-Z]/) && 
                      testimonial.message.length < 500 ? 'en' : 'th'
      
      await turso.execute({
        sql: `INSERT OR REPLACE INTO testimonials 
              (id, avatar, date, message, language, is_active, sort_order, created_at, updated_at) 
              VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        args: [
          testimonial.id,
          testimonial.avatar,
          testimonial.date,
          testimonial.message,
          language,
          1, // is_active
          0, // sort_order
          new Date().toISOString(),
          new Date().toISOString(),
        ]
      })
    }

    console.log(`Successfully seeded ${testimonialData.length} testimonials`)
    
    // Verify the data
    const result = await turso.execute('SELECT COUNT(*) as count FROM testimonials')
    console.log(`Total testimonials in database: ${result.rows[0].count}`)
    
  } catch (error) {
    console.error('Error seeding testimonials:', error)
  }
}

